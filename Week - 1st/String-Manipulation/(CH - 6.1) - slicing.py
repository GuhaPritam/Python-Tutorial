"""
String Slicing

string[start index : end index : steps] (indexing operator = [])

Definition :-
     In Python, [] (square brackets) are referred to as the indexing operator or subscript operator. They are used to
     access specific elements or parts of various data structures such as lists, tuples, strings, etc.

"""
data = '01234567'
print(data[2:5])
print(data[-6:-3])
print(data[::3])
